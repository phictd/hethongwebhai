	<h2>I-Quá trình hình thành:</h2>
    <p>Công ty cổ phần Shop Công Nghệ (Mobile World Joint Stock.) thành lập vào tháng 03/2004, lĩnh vực hoạt động chính của công ty bao gồm: mua bán sửa chữa các thiết bị liên quan đến điện thoại di động, thiết bị kỹ thuật số và các lĩnh vực liên quan đến thương mại điện tử. </p>
    <p>Bằng trải nghiệm về thị trường điện thoại di động từ đầu những năm 1990, cùng với việc nghiên cứu kỹ tập quán mua hàng của khách hàng Việt Nam, Shop Công Nghệ đã xây dựng một phương thức kinh doanh chưa từng có ở Việt Nam trước đây. Công ty đã xây dựng được một phong cách bán hàng tư vấn đặc biệt nhờ vào một đội ngũ nhân viên chuyên nghiệp và trang web <a href="#">www.shopcongnghe.com</a> hỗ trợ như là một cẩm nang về điện thoại di động hàng đầu tại Việt Nam. </p>
    <h3>II-Lịch sử phát triển</h3>
   <p> 04/2004, ra quyết định thành lập công ty. 

07/2004, website www.shopcongnghe.com được chính thức ra mắt (các tên miền khác: www.thegioimobi.com, www.shopcongnghe.com) 

10/2004, tại siêu thị Điện Thoại Di Động - 89A Nguyễn Đình Chiểu siêu thị đầu tiên của hệ thống khai trương với chương trình ra mắt ấn tượng “Đi săn Điện Thoại Di Đông kiểu thổ dân”. 

09/2005, khai trương siêu thị thứ 2 của hệ thống, siêu thị ĐTDĐ – 330 Cộng Hoà. 

Trong năm 2006, khai trương thêm 2 siêu thị mới tại: 26 Phan Đăng Lưu, 182 Nguyễn Thị Minh Khai 

Đến nay, các hệ thống siêu thị của công ty không ngừng được mở rộng ra phạm vi toàn quốc, các website luôn được nâng cấp và đầu tư, từng bước gây được tiếng vang trong thị trường Thương mại điện tử Việt Nam.</p>
